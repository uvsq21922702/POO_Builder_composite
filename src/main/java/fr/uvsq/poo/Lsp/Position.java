package fr.uvsq.poo.Lsp;

public class Position {

private int x,y;

    public Position(){}

    public Position(int Posx,int Posy){ x=Posx; y=Posy;}

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


}
